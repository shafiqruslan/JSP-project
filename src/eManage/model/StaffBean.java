package eManage.model;

public class StaffBean {
	
	private int staff_id;
	private String staff_name;
	private String staff_birth_date;
	private String staff_address;
	private String staff_contact_num;
	private String staff_salary;
	private String staff_position;
	private String staff_hire_date;
	private String staff_email;
	private String staff_password;
	public boolean valid;
	
	public int getStaff_id() {
		return staff_id;
	}
	public void setStaff_id(int staff_id) {
		this.staff_id = staff_id;
	}
	public String getStaff_name() {
		return staff_name;
	}
	public void setStaff_name(String staff_name) {
		this.staff_name = staff_name;
	}
	public String getStaff_birth_date() {
		return staff_birth_date;
	}
	public void setStaff_birth_date(String staff_birth_date) {
		this.staff_birth_date = staff_birth_date;
	}	
	public String getStaff_address() {
		return staff_address;
	}
	public void setStaff_address(String staff_address) {
		this.staff_address = staff_address;
	}
	public String getStaff_contact_num() {
		return staff_contact_num;
	}
	public void setStaff_contact_num(String staff_contact_num) {
		this.staff_contact_num = staff_contact_num;
	}
	public String getStaff_salary() {
		return staff_salary;
	}
	public void setStaff_salary(String staff_salary) {
		this.staff_salary = staff_salary;
	}
	public String getStaff_position() {
		return staff_position;
	}
	public void setStaff_position(String staff_position) {
		this.staff_position = staff_position;
	}
	public String getStaff_hire_date() {
		return staff_hire_date;
	}
	public void setStaff_hire_date(String staff_hire_date) {
		this.staff_hire_date = staff_hire_date;
	}
	public String getStaff_email() {
		return staff_email;
	}
	public void setStaff_email(String staff_email) {
		this.staff_email = staff_email;
	}
	public String getStaff_password() {
		return staff_password;
	}
	public void setStaff_password(String staff_password) {
		this.staff_password = staff_password;
	}
	public boolean isValid() {
		return valid;
	}
	public void setValid(boolean valid) {
		this.valid = valid;
	}

	
	
	
}
